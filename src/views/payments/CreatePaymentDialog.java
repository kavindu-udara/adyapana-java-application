    private void loadSubjectTableData() {
        DefaultTableModel tableModel = (DefaultTableModel) subjectsTable.getModel();
        tableModel.setRowCount(0);

        try {
            ResultSet subjectsResultSet = MysqlConnection.executeSearch("SELECT * FROM `subjects` ");
            while (subjectsResultSet.next()) {
                Vector vector = new Vector();
                vector.add(subjectsResultSet.getString("id"));
                vector.add(subjectsResultSet.getString("name"));

                tableModel.addRow(vector);
            }

            subjectsTable.setModel(tableModel);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadStudentsTableData(String subjectId) {
        DefaultTableModel tableModel = (DefaultTableModel) studentsTable.getModel();
        tableModel.setRowCount(0);

        try {
            ResultSet studentsSubjectEntrollmentResultSet = MysqlConnection.executeSearch("SELECT * FROM `student_subject_entrollments` WHERE `subject_id`='" + subjectId + "' ");
            while (studentsSubjectEntrollmentResultSet.next()) {
                ResultSet studentResultSet = MysqlConnection.executeSearch("SELECT * FROM `students` WHERE `id`='" + studentsSubjectEntrollmentResultSet.getString("student_id") + "' ");
                if (studentResultSet.next()) {
                    Vector vector = new Vector();
                    vector.add(studentResultSet.getString("id"));
                    vector.add(studentResultSet.getString("name"));

                    tableModel.addRow(vector);
                }
                studentsTable.setModel(tableModel);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

