    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        String price = priceField.getText();

        int subjectRow = subjectsTable.getSelectedRow();
        int studentRow = studentsTable.getSelectedRow();

        if (subjectRow > -1 && studentRow > -1 && !price.isEmpty()) {
            Double priceDouble = Double.parseDouble(price);
            try {
                String subjctIdString = String.valueOf(subjectsTable.getValueAt(subjectRow, 0));
                String studentIdString = String.valueOf(studentsTable.getValueAt(studentRow, 0));
                paymentPanel.storeData(studentIdString, subjctIdString, priceDouble);
                dispose();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Please select at least one field!");
        }

    }//GEN-LAST:event_createButtonActionPerformed

